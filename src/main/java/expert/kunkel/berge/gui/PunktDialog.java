	private void okActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_okActionPerformed
		// wenn es ein neuer Punkt ist
		if (isNewPunkt) {
			punkt = new Punkt();
		}
		punkt.setBeschreibung(taBeschreibung.getText());
		punkt.setHoehe(Integer.parseInt(tfHoehe.getText()));
		punkt.setName(tfName.getText());
		punkt.setName2(tfName2.getText());
		punkt.setTyp((Punkttyp) cbTyp.getSelectedItem());
		punkt.setUrl(tfUrl.getText());

		// jetzt noch den Punkt basteln
		Coordinate c = new Coordinate(Double.parseDouble(tfOst.getText()),
				Double.parseDouble(tfNord.getText()), Integer.parseInt(tfHoehe
						.getText()));
		CoordinateArraySequence cas = new CoordinateArraySequence(
				new Coordinate[] { c });
		Point point = new Point(cas, new GeometryFactory());
		punkt.setLage(point);

		// wenn es ein neuer Punkt ist
		if (isNewPunkt) {
			punktDao.insertPunkt(punkt);
		} // wenn es ein bestehender Punkt ist
		else {
			punktDao.updatePunkt(punkt);
		}

		((Berge) this.getParent()).reloadPunkte();

		this.setVisible(false);
	}// GEN-LAST:event_okActionPerformed

