	private void setData(Punkt punkt) {
		this.punkt = punkt;

		if (punkt != null) {
			if (punkt.getHoehe() != null) {
				tfHoehe.setText(Integer.toString(punkt.getHoehe()));
			} else {
				tfHoehe.setText("");
			}
			taBeschreibung.setText(punkt.getBeschreibung());
			tfName.setText(punkt.getName());
			tfName2.setText(punkt.getName2());
			tfNord.setText(Double.toString(punkt.getLage().getCoordinate().y));
			tfOst.setText(Double.toString(punkt.getLage().getCoordinate().x));
			tfUrl.setText(punkt.getUrl());

			cbTyp.setSelectedItem(punkt.getTyp());
		}
	}

	private void clean() {
		punkt = null;
		isNewPunkt = true;
		tfHoehe.setText("");
		taBeschreibung.setText("");
		tfName.setText("");
		tfName2.setText("");
		tfNord.setText("");
		tfOst.setText("");
		tfUrl.setText("");
		cbTyp.setSelectedIndex(6);
	}
